public class JVL_04_Literals {
    public static void main(String[] args) {

        // Byte , short & Int are Integer Literals
        byte age = 34;
        int age2 = 56;
        char ch = 'A';

        // for float we used f or F
        float f1 = 5.6f;

        // for float we used d or D
        double d1 = 4.66D;

        // boolean has 2 values true or false 
        boolean b1 = true;

        System.out.println(age);

        String str = "Iron Man";
        System.out.println(str);
        
    }
}